                        <h2>Modern, Classy, Underwater</h2>
                        <p>Begun in 2018, Underwater Restaurant sought to marry the concepts of an aquarium and a
                            restaurant into one nautical experience. While dining in our aquarium themed restaurant 
                            you'll find an array of fish, a variety of crustaceans, and a plethora of life from the 
                            sea floor. The atmosphere is calm and casual while the aesthetic is like an undersea voyage 
                            in a private submarine. Our commitment to service is second only to our refined dishes. 
                            Come on down to Underwater Restaurant for an aquatic experience like no other.
                        </p>
