                        <h2>Chef de Cuisine, Cliff Murdoch </h2>
                        <p>Inspired by nature, art and photography, Chef Cliff possesses a deep passion for food and
                            boundless creativity. Originally from Newmarket, Ontario, Cliff headed west for Vancouver 
                            as soon as he turned 17. There, he found his culinary calling as he trained with French 
                            chef Frederic Couton and worked towards his Red Seal. In the spring of 2007, Chef Cliff 
                            made the leap to the U.K., where he landed a Chef de Partie position at The Ritz London in
                            Piccadilly, where he gained invaluable training and experience under Chef John Williams. He 
                            was also lucky enough to stage at Restaurant Gordon Ramsay in London and Auberge de l’Île Barbe in Lyon, France.
                            <span>In the winter of 2008, Chef Cliff joined the team at The Bingham Hotel as Senior Sous Chef,
                                helping the restaurant earn its first Michelin star. He spent three years working with the talented
                                Chef Shay Cooper, learning and developing his understanding of food evolution and the finer details of
                                growing, sourcing, cooking and plating. By 2012, Chef Cliff was ready to forge his own path. He found a head
                                chef position that allowed him to focus his attention to modern British cooking with French influences,
                                seasonal ingredients, and beautiful plating with great attention to detail.
                                With his move back to Canada, Chef Cliff is excited to be taking the reins at Underwater Restaurant, where
                                he looks forward to leading the next generation of great chefs and showcasing his classical
                                cooking style with a distinctly modern flare.
                            </span>
                        </p>
